  if (!content && !videoId) {
    throw new ApiError(402, "Content and video is missing");
  }

  const video = await Video.findById(videoId);

  if (!video) {
    throw new ApiError(404, "Video not found");
  }

  const comment = await Comment.create({
    content,
    video: video._id,
    owner: req.user._id,
  });

